ax = int(input("Введите координату X точки A \n"))
ay = int(input("Введите координату Y точки A \n"))
bx = int(input("Введите координату X точки B \n"))
by = int(input("Введите координату Y точки B \n"))
print("\n Извините, в следующий раз я использую парсинг для ввода координат точек \n")

d = round((((bx - ax) ** 2) + ((by - ay) ** 2)) ** 0.5, 3)
print(f'A ({ax}, {ay}); B ({bx}, {by}) -> {d}')